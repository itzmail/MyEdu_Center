/*
* Tipe Data Char ini kalau di bahasa lain seperti variable, tapi yang membedakan adalah Char di sini lebih spesifik
* Data Character (huruf) = value,
* Syntax char => char h = 'h'; HARUS pakai petik satu*/

public class TipeDataChar {
    public static void main(String[] args){
        char m = 'm';
        char a = 'a';
        char n = 'n';
        char t = 't';
        char A = 'a';
        char p = 'p';

        System.out.print(m);
        System.out.print(a);
        System.out.print(n);
        System.out.print(t);
        System.out.print(A);
        System.out.print(p);
    }
}
