public class TernaryOperator {
    public static void main (String[] args){
        /*Ternary Operator
        * Ternary operator adalah operator sederhana dari if statement
        * Ternary operator terdiri dari kondisi yang dievaluasi jijka menghasilkan true maka nilai pertama diambil, jika false maka nilai kedua diambil
        * */

        var nilai = 89;
        String Graduation = nilai >= 70 ? "Anda lulus dengan membanggakan" : "Anda Lulus aja sana, ingat! jangan pernah kembali lagi";

        System.out.println(Graduation);
    }
}
